a = float(input())
b = float(input())
c = float(input())
d = float(input())
mult = a * b * c * d
print (f"O produto dos 4 números é igual a {mult}.")
if mult > 0:
    print ("O produto dos 4 números é positivo.")
elif mult < 0:
    print ("O produto dos 4 números é negativo.")
else:
    print ("O produto dos 4 números é igual a 0.")